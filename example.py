#numeric data types in Python
number_1 = 10
number_2 = 3
print("Addition:", number_1 + number_2)        # Addition
print("Subtraction:", number_1 - number_2)     # Subtraction   
print("Multiplication:", number_1 * number_2)  # Multiplication
print("Division:", number_1 / number_2)        # Division
print("Integer Division:", number_1 // number_2)  # Integer Division
print("Modulus:", number_1 % number_2)         # Modulus
print("Exponentiation:", number_1 ** number_2) # Exponentiation